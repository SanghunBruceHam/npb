// CLI execution
async function main() {
    const seeder = new DatabaseSeeder();
    
    try {
        await seeder.seed();
        console.log('\nüéâ Database seeding completed successfully!');
        console.log('üí° You can now start the server: npm start');
        
    } catch (error) {
        console.error('\n‚ùå Seeding failed:', error.message);
        
        if (error.code === 'ECONNREFUSED') {
            console.error('üí° Make sure PostgreSQL is running');
        } else if (error.code === '3D000') {
            console.error('üí° Database does not exist. Run: npm run db:migrate');
        } else if (error.code === '42P01') {
            console.error('üí° Tables do not exist. Run: npm run db:migrate');
        }
        
        process.exit(1);
    } finally {
        await seeder.close();
    }
}

